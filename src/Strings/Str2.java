package Strings;

public class Str2 {
    public static void main(String[] args) {
        String name = "Govind";
        System.out.println(name.length());
        System.out.println(name.charAt(1));
        System.out.println(name.concat(" Mahto"));
        System.out.println(name);
        System.out.println(name.contains("Mahto"));
        System.out.println(name.equals("Govind"));
        System.out.println(name.equalsIgnoreCase("govind"));
        System.out.println(name.length());
    }
}
