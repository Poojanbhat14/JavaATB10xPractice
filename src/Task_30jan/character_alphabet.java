package Task_30jan;

import java.util.Scanner;

public class character_alphabet {
    public static void main(String[] args) {
        char c='c';
        if ((c >= 'a' && c <= 'z')||(c >= 'A' && c <= 'Z')){
            System.out.println("Character is alphabet");
        }else {
            System.out.println("Character is not a alphabet");
        }
    }
}
